export function fmtAgeRange(min, max) {
  const f = (m) => (m < 12) ? (m + 'mo') : (m % 12 === 0) ? (m / 12) + 'y' : (m / 12).toFixed(1) + 'y';
  return f(min) + '–' + f(max);
}
